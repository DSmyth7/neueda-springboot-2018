
public class Toy {
	
	private String Character, Forename, Surname;
	
	

	public Toy(String character, String forename, String surname) {
		super();
		Character = character;
		Forename = forename;
		Surname = surname;
	}

	public String getCharacter() {
		return Character;
	}

	public void setCharacter(String character) {
		Character = character;
	}

	public String getForename() {
		return Forename;
	}

	public void setForename(String forename) {
		Forename = forename;
	}

	public String getSurname() {
		return Surname;
	}

	public void setSurname(String surname) {
		Surname = surname;
	}
	
	

}
